<section class="wp-section" id="problema">
  <h2><span class="section-number">2</span> El Problema</h2>

  <p>El modelo editorial tradicional presenta varias fricciones para los autores y para las comunidades lectoras. Los escritores suelen enfrentarse a regalías bajas (entre 7% y 25% sobre importes netos), estructuras contractuales complejas y gran opacidad en los reportes de ventas, lo que dificulta proyectar ingresos y tomar decisiones de largo plazo.</p>

  <p>Por el lado de los lectores, la relación con la obra se mantiene casi exclusivamente en el plano del consumo: compran el libro, lo leen y, en el mejor de los casos, recomiendan la obra, pero <strong>no participan de manera estructurada en el potencial económico</strong> ni en la evolución narrativa del universo literario.</p>

  <p>A esto se suma que la gestión de derechos y regalías de propiedad intelectual se apoya todavía en procesos manuales y bases de datos cerradas, que dificultan la auditoría, favorecen errores y retrasos, y dejan a los autores en desventaja.</p>

  <h3>En síntesis, el ecosistema actual carece de:</h3>
  <ul>
    <li>Herramientas transparentes y programables para gestionar y distribuir regalías.</li>
    <li>Mecanismos de financiamiento alineados con el éxito real de la obra y accesibles para autores independientes.</li>
    <li>Modelos que integren a lectores y comunidades como partícipes económicos y culturales del universo narrativo.</li>
  </ul>
