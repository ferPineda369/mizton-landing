<section class="wp-section" id="resumen">
  <h2><span class="section-number">1</span> Resumen Ejecutivo</h2>

  <p>El token <strong>KIMEN</strong> es un activo digital BEP-20 emitido en la red BNB Smart Chain (BSC) que representa la puerta de entrada al universo literario de la saga KIMEN, comenzando con la obra <em>"Un Bello Mundo por Extrañar"</em>. KIMEN combina un fuerte componente de utilidad cultural (acceso a contenido exclusivo, votaciones y perks) con un modelo transparente de participación en los flujos de regalías del libro, alineando los incentivos del autor, la comunidad y los primeros participantes.</p>

  <p>El suministro total es de <strong>4,800 KIMEN</strong>, diseñado como una emisión escasa y no inflacionaria, distribuida entre venta pública, autor/creador, marketing/comunidad y reservas para futuras expansiones de la saga. La venta pública de 800 KIMEN a un precio fijo de <strong>$25 USD por token</strong> tiene como objetivo recaudar <strong>$20,000 USD</strong> destinados principalmente al financiamiento del escritor, la producción editorial y el lanzamiento inicial del proyecto.</p>

  <p>El modelo de reparto de regalías asigna el <strong>20% de las regalías netas</strong> de la obra a la comunidad: un 15% se distribuye a los tenedores de KIMEN de forma proporcional a su tenencia, mediante un mecanismo de reclamación ("claim") on-chain en BSC, y un 5% se asigna a Mizton, que actúa como un tenedor más y participa bajo el mismo esquema de distribución. El 80% restante de las regalías se mantiene para el autor y las necesidades de desarrollo de la obra, reforzando la sostenibilidad creativa a largo plazo.</p>

  <p>Operativamente, las regalías netas se consolidan off-chain, se convierte el porcentaje correspondiente a la comunidad en stablecoins y se depositan periódicamente en un contrato inteligente de distribución en BSC. A partir de snapshots de tenencia de KIMEN, cada participante puede reclamar su parte de las regalías en stablecoins directamente desde la dApp del proyecto, sin intermediarios y con total trazabilidad on-chain.</p>

  <p>KIMEN busca demostrar un modelo replicable de tokenización de propiedad intelectual literaria, donde los lectores dejan de ser únicamente consumidores pasivos para convertirse en <strong>cocreadores y partícipes del éxito económico y narrativo</strong> de la obra.</p>

